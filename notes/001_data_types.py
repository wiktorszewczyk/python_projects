int             # integer type variable f.e. 1; 50; 20109202910.

                # Bases other than Decimal 
                # 0b / 0B - Binary f.e. print(0b10) returns 2
                # 0o / 0O - Octal f.e. print(0o10) returns 8
                # 0x / 0X - Hexadecimal f.e. print(0x10) returns 16


float           # float type variable contains floating-point number f.e. 1,5; 3.14159265; 999999.000001 
                # RANGE -  max = 1.8e308 ~ inf | min = 5.0e-324 ~ 0.0


complex         # complex numbers are specified as <real part>+<imaginary part>j f.e. 3j+2; 13+7j

str             # string type variable contains both letters and number f.e. "5 foxes", "I hate myself." 
                # delimited using " " or ' '

bool            # boolean variable is used to represent the truth value of an expression f.e. 1 < 2 returns True (1), 0 == 1 returns False (0)

list            # list type is used to store multiple arguments in one variable
